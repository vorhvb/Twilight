.code16
.globl _start

# ──────────────────────────────────────────────────────────────────────

_start:
	movw $0x0003, %ax
	int $0x10

	movw $0x0208, %ax
	movw $0x7E00, %bx
	movw $0x0002, %cx
	int $0x13

	jc error

	ljmp $0x07E0, $0x0000

error:
	movb $0x0E, %ah
	movb $'E', %al
	int $0x10

	cli
	hlt

# ──────────────────────────────────────────────────────────────────────

.org 420
.word 0xAA55

